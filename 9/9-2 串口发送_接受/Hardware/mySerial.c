void USART1_IRQHandler(){
	if (USART_GetFlagStatus(USART1, USART_FLAG_RXNE) == SET){
		Serial_RxData = USART_ReceiveData(USART1);
		Serial_RxFlag = 1;
		USART_ClearITPendingBit(USART1, USART_FLAG_RXNE);
	}

}
